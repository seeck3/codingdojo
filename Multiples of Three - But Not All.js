

function multiThree(){
    var arr = [];
    
    for(var i = -300; i < 0; i++){
        if(i % 3 === 0){

            if(i !== -3){
                arr.push[i];
               
            }else if(i !== -6){
                arr.push[i];
            }                    
        }      
    }
    return arr;

}

multiThree(); 